import java.util.Scanner;

public class DisplayTime {
    public static void main(String[] args) {
        long aa = 1234567890;
        float bb = (float) aa;
        long cc = (long) bb;
        System.out.println(aa);
        // вывод 1234567890

        System.out.println(bb);
        // вывод 1.23456794E9

        System.out.println(cc);
        // вывод 1234567936
    }

}
